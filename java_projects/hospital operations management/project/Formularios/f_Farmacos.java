    public void llenartxt() throws IOException{
        String Linea;
        Linea = medicina +'|'+ recetado +'|'+ cantidad +'|'+ duracion +'|'+ "\r\n";
        writer = new FileWriter(File,true);
        writer.write(Linea );
        writer.close(); 
    }
