    private void btn_añadir1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_añadir1ActionPerformed
        // TODO add your handling code here:
        medicina = (String) medicine1.getSelectedItem();
        recetado = (String) recetado1.getSelectedItem();
        cantidad = (Integer) spin_cantidad.getValue();
        duracion = (String) duration.getSelectedItem();
        if ("Seleccionar".equals(medicina) || "Seleccionar".equals(recetado) || "Seleccionar".equals(duracion)) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione valores válidos.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (cantidad <= 0) {
            JOptionPane.showMessageDialog(this, "La cantidad debe ser mayor a 0.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        String rutaInventario = "proyect_logs\\Almacenamiento_Farmacos.txt";
        // Intentar restar la cantidad del inventario
        boolean actualizado = f_SincronizarInventario.actualizarInventario(medicina, cantidad, rutaInventario);

        if (!actualizado) {
            // Si no se pudo actualizar el inventario, no continuar
            return;
        }
        // Si se actualizó correctamente, agregar a la tabla y guardar en el archivo de salida
        modelo.addRow(new Object[]{medicina, cantidad, recetado, duracion});
        try {
            llenartxt();
        } catch (IOException ex) {
            Logger.getLogger(f_Farmacos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error al guardar los datos en el archivo.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        JOptionPane.showMessageDialog(this, "Se han añadido correctamente los medicamentos muchas gracias por usar las boletas Farmacia.");
    }//GEN-LAST:event_btn_añadir1ActionPerformed

