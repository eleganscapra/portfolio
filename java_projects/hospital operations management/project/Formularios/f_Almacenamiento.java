    private void btn_añadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_añadirActionPerformed
        // TODO add your handling code here:
        codigo = (String)jtxt_cod.getText();
        farmaco = (String) jtxt_farm.getText();
        cantidad = Integer.parseInt(jtxt_cant.getText());
        concentracion = (String) jtxt_conc.getText();
        almacenamiento.addRow(new Object[]{codigo, farmaco, cantidad, concentracion});

        try {
            llenado_inv();  // Guardamos en el archivo
        } catch (IOException ex) {
            Logger.getLogger(f_Farmacos.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error al guardar los datos en el archivo.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_añadirActionPerformed

