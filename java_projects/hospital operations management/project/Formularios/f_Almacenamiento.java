    public void llenado_inv() throws IOException{
        String Linea;
        Linea = codigo +'|'+ farmaco +'|'+ cantidad +'|'+ concentracion +'|'+ "\r\n";
        writer = new FileWriter(mira,true);
        writer.write(Linea);
        writer.close(); 
    }
